#ifndef RAY_MATH_H
#define RAY_MATH_H

///////////////////////////////////////////////////////////////////////////////
// DEFINES
///////////////////////////////////////////////////////////////////////////////
#define SQUARE(x) ({ typeof (x) _x = (x); _x * _x; })           ///< Square a number
#define CUBE(x) ({ typeof (x) _x = (x); _x * _x * _x; })        ///< Cube a number
#define TESS(x) ({ typeof (x) _x = (x); _x * _x * _x * _x; })   ///< Tesseract a number

#endif//RAY_MATH_H
